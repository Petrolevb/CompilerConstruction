import java.util.Scanner;

class Runtime
{
    static void printString(String s) { System.out.println(s); }
    static void printInt(int i) { System.out.println(i); }
    static void printDouble(double d) { System.out.println(d); }
    static int readInt() { return scanner.nextInt(); }
    static double readDouble() { return scanner.nextDouble(); }
    
    static Scanner scanner = new Scanner(System.in);

}
